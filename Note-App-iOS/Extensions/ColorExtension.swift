import UIKit

extension Color {
    static let onPrimaryGreen = Color("YellowGreen")
    static let onSecondaryRed = Color("Persimmon")
    static let onBackgroundWhite = Color("Alabaster")
}

extension UIColor {
    static let onPrimaryGreen = UIColor(named: "YellowGreen")!
    static let onSecondaryRed = UIColor(named: "Persimmon")!
    static let onBackgroundWhite = UIColor(named: "Alabaster")!
}
